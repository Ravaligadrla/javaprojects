package Welcome.loopingstatements;

public class ProductInformation {
         public String pname;
         private  long pno;
         public double prate;
         public double pdisc;

    public long getPno() {
        return pno;
    }

    public void setPno(long pno) {
        this.pno = pno;
    }

    public double getPrate() {
        return prate;
    }

    public void setPrate(double prate) {
        this.prate = prate;
    }

    public double getPdisc() {
        return pdisc;
    }

    public void setPdisc(double pdisc) {
        this.pdisc = pdisc;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }
}

